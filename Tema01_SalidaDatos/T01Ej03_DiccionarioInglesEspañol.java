package Tema01_SalidaDatos;

/**
 * Tema 1 Ejercicio 3
 *
 * Escribe un programa que muestre por pantalla 10 palabras en inglés junto a su
 * correspondiente traducción al castellano. Las palabras deben estar
 * distribuidas en dos columnas y alineadas a la izquierda. Pista: Se puede
 * insertar un tabulador mediante \t.
 *
 * @author Javier Roviralta Terrón
 */
public class T01Ej03_DiccionarioInglesEspañol {

  public static void main(String[] args) {

    System.out.println("Hello\tHola");
    System.out.println("Car\tCoche");
    System.out.println("Before\tAntes");
    System.out.println("Monday\tLunes");
    System.out.println("Blue\tAzul");
    System.out.println("To do\tHacer");
    System.out.println("Cat\tGato");
    System.out.println("Foot\tPie");
    System.out.println("Shirt\tCamisa");
    System.out.println("House\tCasa");
  }
}
