package Tema01_SalidaDatos;

/**
 * Tema 1 Ejercicio 6
 *
 * Escribe un programa que pinte por pantalla una pirámide rellena a base de
 * asteriscos. La base de la pirámide debe estar formada por 9 asteriscos.
 *
 * @author Javier Roviralta Terrón
 */
public class T01Ej06_PiramideRellena {

  public static void main(String[] args) {

    System.out.println("");
    System.out.println("\t\t\t\t*");
    System.out.println("\t\t\t*\t*\t*");
    System.out.println("\t\t*\t*\t*\t*\t*");
    System.out.println("\t*\t*\t*\t*\t*\t*\t*");
    System.out.println("*\t*\t*\t*\t*\t*\t*\t*\t*");
    System.out.println("");
  }
}
